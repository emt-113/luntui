/*********************************************************************************************************************
 * 文件名称          auto_menu
 * 功能描述          通用多级菜单框架 - 支持 int/uint/float/ReadOnly 类型
 * 平台              CYT4BB + IAR + TFT180 (128x160)
 *
 * 核心特性：
 * 1. 两级菜单系统：Page(分类) -> Item(条目)
 * 2. 状态机架构：NAV_MODE(浏览) / EDIT_MODE(编辑)
 * 3. 多类型支持：int32_t, uint32_t, float, readonly
 * 4. 数据回滚机制：编辑退出时自动恢复原始值
 * 5. 高效刷新：仅重绘当前页面，避免全屏闪烁
 *
 * 修改记录
 * 作者              时间                备注
 * 2025-02-06       AutoMenu           first version
********************************************************************************************************************/

#ifndef _AUTO_MENU_H
#define _AUTO_MENU_H

#include "bsp_app.h"

//================================================= 菜单配置宏定义 ==================================================
#define MENU_MAX_ITEMS_PER_PAGE     (8)             // 单页最大条目数 (128x160 屏幕，建议 6-8 个)
#define MENU_CURSOR_X               (0)             // 光标显示 X 坐标
#define MENU_LABEL_START_X          (10)            // 标签起始 X 坐标
#define MENU_VALUE_START_X          (70)            // 数值显示起始 X 坐标（调整给数值更多空间）
#define MENU_LINE_HEIGHT            (16)            // 每行高度 (对应 8x16 字体)
#define MENU_REFRESH_PERIOD_MS      (100)           // 菜单刷新周期 (ms)

//================================================= 菜单数据类型枚举 ==================================================
typedef enum
{
    MENU_TYPE_INT8 = 0,                           // 有符号8位整型 (-128 ~ 127)
    MENU_TYPE_INT16,                              // 有符号16位整型 (-32768 ~ 32767)
    MENU_TYPE_INT32,                              // 有符号32位整型
    MENU_TYPE_UINT8,                              // 无符号8位整型 (0 ~ 255)
    MENU_TYPE_UINT16,                             // 无符号16位整型 (0 ~ 65535)
    MENU_TYPE_UINT32,                             // 无符号32位整型
    MENU_TYPE_FLOAT,                              // 浮点型
    MENU_TYPE_READONLY,                           // 只读类型 (仅显示，不可编辑)
} menu_data_type_enum;

//================================================= 菜单状态枚举 ==================================================
typedef enum
{
    MENU_NAV_MODE = 0,                            // 浏览模式：光标移动，选择条目
    MENU_EDIT_MODE,                               // 编辑模式：修改数值
} menu_state_enum;

//================================================= 菜单条目结构体 ==================================================
typedef struct MenuItem
{
    const char          *label;                   // 显示标签 (如 "速度环 Kp")
    void                *ptr;                     // 变量指针 (指向实际数据)
    menu_data_type_enum type;                     // 数据类型
    menu_data_type_enum actual_type;              // 当 type=READONLY 时的真实数据类型
    union
    {
        int32_t         i32;                      // int32 类型步进/最大/最小值
        uint32_t        u32;                      // uint32 类型步进/最大/最小值
        float           f;                        // float 类型步进/最大/最小值
    } step, max, min;                             // 使用 union 优化内存
    uint8_t             display_num;              // 显示位数（防止屏幕溢出）
    uint8_t             float_point_num;          // 小数点后位数（仅 float 类型有效）
    void                (*callback)(void);        // 确认回调函数 (保存参数等操作)
} menu_item_t;

//================================================= 菜单页面结构体 ==================================================
typedef struct MenuPage
{
    const char          *name;                    // 页面名称
    menu_item_t         *items;                   // 条目数组指针
    uint8_t             item_count;               // 条目数量
} menu_page_t;

//================================================= 全局菜单系统状态 ==================================================
typedef struct MenuSystem
{
    menu_page_t         *pages;                   // 页面数组指针
    uint8_t             page_count;               // 总页面数
    uint8_t             current_page_index;       // 当前页面索引
    uint8_t             current_item_index;       // 当前条目索引

    menu_state_enum     state;                    // 菜单状态 (浏览/编辑)

    // 编辑模式数据备份 (用于回滚)
    union
    {
        int8_t          i8;                       // 8位有符号
        int16_t         i16;                      // 16位有符号
        int32_t         i32;                      // 32位有符号
        uint8_t         u8;                       // 8位无符号
        uint16_t        u16;                      // 16位无符号
        uint32_t        u32;                      // 32位无符号
        float           f;                        // 浮点型
    } backup_val;

    // 编辑模式临时变量 (编辑时修改此值，确认后才写入原变量)
    union
    {
        int8_t          i8;                       // 8位有符号
        int16_t         i16;                      // 16位有符号
        int32_t         i32;                      // 32位有符号
        uint8_t         u8;                       // 8位无符号
        uint16_t        u16;                      // 16位无符号
        uint32_t        u32;                      // 32位无符号
        float           f;                        // 浮点型
    } edit_val;

    // UI 刷新标志 (仅刷新变化的区域)
    uint8_t             refresh_flag;             // 0=无需刷新, 1=刷新全部, 2=刷新当前行
    uint8_t             last_item_index;          // 上次选中的条目索引
} menu_system_t;

//================================================= 菜单系统 API 声明 ==================================================

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     menu_init
// 功能描述     初始化菜单系统
// 输入参数     pages: 页面数组指针
// 输入参数     page_count: 页面总数
// 返回参数     void
// 使用示例     menu_init(menu_pages, 3);
// 注意事项     必须在调用其他菜单函数前初始化
//-------------------------------------------------------------------------------------------------------------------
void menu_init(menu_page_t *pages, uint8_t page_count);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Menu_Key_Handler
// 功能描述     菜单按键处理函数 (由按键任务直接调用)
// 输入参数     key_val: 按键值 (1=上, 2=下, 3=确认, 4=退出)
// 返回参数     void
// 使用示例     Menu_Key_Handler(key_down);
// 注意事项     此函数不含屏幕刷新代码，仅处理逻辑
//-------------------------------------------------------------------------------------------------------------------
void Menu_Key_Handler(uint8_t key_val);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     Menu_Refresh_Task
// 功能描述     菜单刷新任务 (由调度器定时调用，如 100ms)
// 返回参数     void
// 使用示例     添加到 scheduler_task 数组中
// 注意事项     仅刷新当前页面，避免全屏闪烁
//-------------------------------------------------------------------------------------------------------------------
void Menu_Refresh_Task(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数名称     menu_get_current_state
// 功能描述     获取当前菜单状态 (调试用)
// 返回参数     menu_state_enum: 当前状态
//-------------------------------------------------------------------------------------------------------------------
menu_state_enum menu_get_current_state(void);


void my_menu_init();
#endif // _AUTO_MENU_H
